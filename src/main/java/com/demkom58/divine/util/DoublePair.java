package com.demkom58.divine.util;

public class DoublePair {
    private final double d1;
    private final double d2;

    public DoublePair(double d1, double d2) {
        this.d1 = d1;
        this.d2 = d2;
    }

    public double getD1() {
        return d1;
    }

    public double getD2() {
        return d2;
    }
}
